public class IfExemplo {
	public static void main (String [] args) {
		
		int num = 70;
				
		if (num < 100){
			System.out.println("O número " + num + " é menor que 100");
		} else {
			System.out.println("O número " + num + " não é menor que 100");
		}
	}
}